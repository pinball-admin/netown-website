export function getTournamentYears(): number[] {
  return Object.keys(worldCupHistory).map(Number).sort((a, b) => a - b)
}

export function getOverlappingTeams(year: number): string[] {
  const tournament = worldCupHistory[year]
  if (!tournament) return []

  const { teamsData } = require('./teams-data') as typeof import('./teams-data')
  const codes2026 = new Set(Object.values(teamsData).map(t => t.code))

  return tournament.participatingTeams
    .filter(t => codes2026.has(t.code))
    .map(t => t.code)
}
